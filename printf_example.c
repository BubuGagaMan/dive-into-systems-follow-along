#include <stdio.h>

int main(int argc, char *argv[]) {
	// string literal 
	printf("Hello, my name is %s\n", "Bubu");
	// integer
	printf("Printing some integers: %d, %d\n", 1, 2);
	// float
	printf("Printing some float: %g\n", 1.1);
	// char
	printf("Printing some char: %c\n", 'a');

    return 0;
}
